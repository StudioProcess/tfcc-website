          <b-col class="cmpx-30" lg="5" offset-lg="1">
            <p class="f700 text-uppercase mb-4">Learning to create new content</p>
            <p class="mb-4">For realizing this project we use a machine learning technique called <strong>GAN</strong> (Generative Adversarial Network). In contrast to many other ML algorithms, which detect or classify images, this technique is capable of generating images – <strong>it creates new content!</strong></p>
            <p class="mb-4">The first step is to provide many samples for the type of image to generate. This is called the dataset. To create our <strong>dataset</strong>, we use symbols and symbol-like characters from a number of free fonts and icon sets. We use sorting and filtering in combination with hand-picking the symbols that match the complexity and aesthetics we are looking for. Using our own dataset-browser, we selected about <strong>7.000 symbols</strong> (out of over 200.000) for our initial dataset.</p>
            <p class="mb-4">In the next step we assign <strong>'meaning'</strong> to the icons in the dataset. This is done through a labeling process. It is the essential human contribution in shaping the machine algorithm. Our label set consists of about <strong>40 unique terms</strong> and focuses on (natural and artificial) resources, qualities and actions.</p>
            <p class="mb-6 mb-lg-0">We want to make labeling a collaborative process by inviting artists and designers to fill out an online questionnaire, which shows them a random series of symbols from the dataset with the task to pick an appropriate label for each one.</p>
          </b-col>
          <b-col lg="5" offset-lg="1" class="cmpx-0 pt-lg-8">
