          </b-col>
        </b-row>
        <b-row class="cmmx-0 mt-8">
          <b-col class="cmpx-30" lg="5" offset-lg="2" order-lg="2">
            <p class="mb-4">Now the dataset including labels can be used to <strong>'train'</strong> an artificial neural network. In this stage the samples are repeatedly fed into the network, its results evaluated and the network tweaked to produce better results. The AI learns the visual characteristics of the symbols it sees, as well as the association of labels to these visual representations. This step is somewhat time intensive and needs lots of computing power.</p>
            <p class="mb-4">Finally, the trained model can be used to <strong>generate new tokens</strong>. Given an input of three words from the set of labels it will produce tokens that best represent these concepts, based on what it has learned previously. Since the tokens are pixel images at that point they will be vectorized in order to be universally usable in a graphic design context.</p>
            <p class="mb-6 mb-lg-0">At this stage Tokens for Climate Care is set up as an <strong>art project</strong>. We see it as a chance to experiment with machine learning in the context of graphic design, our main field of work.<br>The goal of this project: to implement a <strong>fully working prototype</strong>, which we can publish and gather feedback on. It will enable us to judge commercial viability, for example as a <strong>logo generator</strong>. This will incur additional costs for commercially licensing big datasets and the extensive labeling required.</p>
